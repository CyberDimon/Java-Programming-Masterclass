package com.CyberDimon.Chapter7.CompositionChallenge;

public class Dimension {
    private int x;
    private int y;
    private int z;

    public Dimension(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
